package MathClass;
import java.lang.Math;
public class AdditionWithMathClass {
    public static void main(String[] args) {

        System.out.println("Addition:"+Math.addExact(30,20));
        System.out.println("Maximum: "+Math.max(500,350));
        System.out.println("Minimum: "+Math.min(500,350));
        System.out.println("Multiply: "+Math.multiplyExact(5,5));
        System.out.println("Square Root: "+Math.sqrt(49));
        System.out.println("Subtraction: "+Math.subtractExact(40,20));
        System.out.println("Increment: "+Math.incrementExact(7));
        System.out.println("Decrement: "+Math.decrementExact(7));
        System.out.println("Power: "+Math.pow(4,2));
    }
}
